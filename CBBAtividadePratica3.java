/**
 * Crie um programa que calcule a área de um retângulo, declare e inicialize
 *  duas variáveis para testá-lo, imprimindo esses resultados na tela
 */

public class CBBAtividadePratica3 {
    public static void main(String[] args) {
        /* Declarando e inicializando as duas varíaveis  */
        int altura = 20;
        int base = 30;

        /* Calaculando a area do triangulo */
        int area = base*altura;

        /* Imprimindo o resultado na tela */
        System.out.println("A área do triângulo, é: " + area);
    }
}
